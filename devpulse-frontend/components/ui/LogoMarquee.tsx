export function LogoMarquee() {
  const trackRef = useRef<HTMLDivElement>(null);
  const outerRef = useRef<HTMLDivElement>(null);
  const [activeName, setActiveName] = useState("OpenAI");

  useEffect(() => {
    let rafId: number;
    const detect = () => {
      if (!trackRef.current || !outerRef.current) return;
      const outerRect = outerRef.current.getBoundingClientRect();
      const centerX = outerRect.left + outerRect.width / 2;
      const items = trackRef.current.querySelectorAll(".marquee-item");
      let closest: Element | null = null;
      let closestDist = Infinity;

      items.forEach((item) => {
        const rect = item.getBoundingClientRect();
        const itemCenterX = rect.left + rect.width / 2;
        const dist = Math.abs(itemCenterX - centerX);
        if (dist < closestDist) {
          closestDist = dist;
          closest = item;
        }
      });

      if (closest) {
        const name = (closest as HTMLElement).dataset.name;
        if (name) setActiveName(name);
      }
      rafId = requestAnimationFrame(detect);
    };
    rafId = requestAnimationFrame(detect);
    return () => cancelAnimationFrame(rafId);
  }, []);

  return (
    <div className="marquee-wrapper">
      <div className="marquee-header max-w-[1280px] mx-auto px-6">
        <p className="marquee-label">Works perfectly with</p>
        <p className="marquee-active-name">{activeName}</p>
      </div>
      <div className="marquee-outer" ref={outerRef}>
        <div className="marquee-track" ref={trackRef}>
          {[...logos, ...logos].map((logo, i) => (
            <div key={i} className="marquee-item" data-name={logo.name}>
              <span className="marquee-icon" dangerouslySetInnerHTML={{ __html: logo.icon }} />
              <span className="marquee-name">{logo.name}</span>
            </div>
          ))}
        </div>
      </div>
    </div>
  );
}
